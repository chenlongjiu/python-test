'''
Follow up for "Unique Paths":

Now consider if some obstacles are added to the grids. How many unique paths would there be?

An obstacle and empty space is marked as 1 and 0 respectively in the grid.

For example,

There is one obstacle in the middle of a 3x3 grid as illustrated below.

[
  [0,0,0],
  [0,1,0],
  [0,0,0]
]

The total number of unique paths is 2.

Note: m and n will be at most 100.
'''

class Solution(object):
	def uniquePathsWithObstacles(self, obstacleGrid):
		if not obstacleGrid:
			return 0
		#cannot start and cannot finish
		if obstacleGrid[0][0] == 1 or obstacleGrid[-1][-1] == 1:
			return 0  
		rel = [[0] * len(obstacleGrid[0]) for _ in xrange(len(obstacleGrid))]
		rel[0][0] = 1
		for row in xrange(0,len(obstacleGrid)):
			for col in xrange(0,len(obstacleGrid[0])):
				if obstacleGrid[row][col] != 1:
					if row - 1 >= 0:
						rel[row][col] += rel[row-1][col]
					if col - 1  >= 0:
						rel[row][col] += rel[row][col-1]
		return rel[-1][-1]

